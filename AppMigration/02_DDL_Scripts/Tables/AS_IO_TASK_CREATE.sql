-- Source Appian CDT: AS_IO_Task
-- Description: Run-time task which represents a front-end task for users
DROP TABLE IF EXISTS AS_IO_TASK CASCADE;
CREATE TABLE AS_IO_TASK (
    TASK_ID INT PRIMARY KEY,
    REQUEST_ID INT,
    TASK_REF_ID INT,
    TASK_NAME VARCHAR(255),
    TASK_DESC VARCHAR(255),
    TASK_BEHAVIOR_TYPE_ID INT,
    TASK_CATEGORY_ID INT,
    DUE_DATE DATE,
    SLA_DAYS INT,
    SLA_RULE VARCHAR(255),
    GROUP_ASSIGNEE INT,
    USER_ASSIGNEE VARCHAR(255),
    TASK_STATUS_ID INT,
    TASK_DOC_UPLOAD_ID INT,
    REQUIRED_ONBOARDING_DOCUMENT_ID INT,
    TASK_REVIEW_ID INT,
    ORDER_NUMBER INT,
    COMMENT_ID INT,
    AVAILABLE_DATETIME TIMESTAMP,
    COMPLETED_BY VARCHAR(255),
    COMPLETED_DATETIME TIMESTAMP,
    CREATED_BY VARCHAR(255),
    CREATED_DATETIME TIMESTAMP,
    MODIFIED_BY VARCHAR(255),
    MODIFIED_DATETIME TIMESTAMP,
    QUESTIONNAIRE_ID INT,
    UNDERSTANDING_ID INT,
    NORTHROW_CHECK_UUID VARCHAR(255),
    DOC_SIGNATURE_CONTEXT_ID INT,
    BENCHMARK INT,
    RPA_EXECUTION_ID VARCHAR(255),
    RETRY_RPA_EXECUTION BOOLEAN,
    NEW_CUSTOMER_ADDRESS_ID INT
);
