-- Description: Cdt for customer portal Benchmark
DROP TABLE IF EXISTS AS_IO_CUSTOMER_BENCHMARK CASCADE;
CREATE TABLE AS_IO_CUSTOMER_BENCHMARK (
    BENCHMARK_ID INT PRIMARY KEY,
    NAME VARCHAR(255),
    IDENTIFIER VARCHAR(255),
    TYPE VARCHAR(255),
    AMOUNT DOUBLE PRECISION,
    CURRENCY VARCHAR(255),
    HEDGE VARCHAR(255),
    IS_LICENSE_ACQUIRED BOOLEAN,
    CUSTOMER_ID INT
);
